"""CrewBoardStore — SQLite-backed CRUD for the kanban.

Thin layer over the schema in `gateway.crew_board.schema`. Lives in
the same DB as the vault index (`.vault-writer/vault.db`) but never
touches the notes / FTS / vec tables. Audit log entries are written
on every state-mutating call so the trail is automatic.

Sync API. Callers in async paths should wrap in `asyncio.to_thread`
when on a hot path; SQLite is fast enough that for the typical
board operation (a handful of rows) this is overkill.
"""

from __future__ import annotations

import json
import sqlite3
import threading
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any, Iterable

from gateway.crew_board import schema


@dataclass
class Board:
    board_id: str
    name: str
    description: str
    created_at: str = ""


@dataclass
class Project:
    slug: str
    path: str
    name: str
    enabled: bool = False
    push_allowed: bool = False
    test_cmd: str | None = None
    created_at: str = ""
    updated_at: str = ""
    # Real "last worked on" timestamp (ISO-8601 UTC) = max(git HEAD commit
    # time, dir mtime), set by the scanner. Use this for recency sorting,
    # NOT updated_at (which is just the last DB upsert / scan time).
    modified_at: str | None = None
    # Opt-in: run each task in its own git worktree and allow >1
    # concurrent task for the assignee (capped by lane count). Default
    # off — the bench-proven model needs both GPUs so single-lane stays
    # the safe default.
    parallel: bool = False


@dataclass
class Task:
    slug: str
    title: str
    body: str = ""
    status: str = schema.STATUS_PROPOSED
    project_slug: str = ""
    assignee: str = "none"
    created_by: str = "owner"
    priority: str = "medium"
    estimate: str | None = None
    acceptance_criteria: list[dict] = field(default_factory=list)
    files_of_interest: list[str] = field(default_factory=list)
    depends_on: list[str] = field(default_factory=list)
    tags: list[str] = field(default_factory=list)
    attempt_count: int = 0
    last_branch: str | None = None
    last_pr_url: str | None = None
    verify_results: dict = field(default_factory=dict)
    created_at: str = ""
    updated_at: str = ""
    # Optional reviewer that must approve a task before it can leave
    # REVIEW → DONE. When None, REVIEW waits for the owner.
    review_by: str | None = None
    # When >2, the hive_agent_loop holds off on auto-done until this
    # many consecutive green pytest runs have happened. Lets polish
    # tasks iterate instead of exiting at first green.
    polish_iters: int | None = None
    # Optional shell command run by the verifier AFTER pytest passes,
    # in the project dir. Non-zero exit fails the tier. Used as the
    # integration smoke gate.
    smoke_cmd: str | None = None
    # Token usage tracked SEPARATELY — never combined. hive = Ollama
    # eval tokens, claude = Claude CLI tokens.
    hive_tokens: int = 0
    claude_tokens: int = 0
    # Last heartbeat from an active runner (ISO/UTC). The reaper uses
    # this to distinguish a live long-running task from a crash-orphan.
    heartbeat_at: str | None = None
    # Agent-turn telemetry (hive loop): total turns + unparseable replies.
    # Powers the stats parse-fail rate via a cheap SUM.
    agent_turns: int = 0
    parse_fails: int = 0
    # Live current action ("turn N · tool target") — what the agent is
    # doing right now, surfaced on the board for live watching.
    last_action: str | None = None
    # Content requests. kind='content' tasks are generated by the Image/Video
    # shims, not a code runner. content_spec holds the request + result media.
    kind: str = "code"
    content_spec: dict = field(default_factory=dict)
    # P6 goal-completion loop: the goal_id this task belongs to (None for
    # tasks not created as part of a /board/decompose goal plan).
    goal_id: str | None = None
    # P2 v-Next: which board this task belongs to. 'default' for all existing
    # tasks (back-compat via ALTER TABLE DEFAULT in schema migrations).
    board_id: str = "default"
    # #198: last-agent handoff summary — "what I did + current state + next
    # step" — plus who wrote it (model/agent) and when. Overwritten on each
    # agent touch so it always reflects the most recent worker.
    last_summary: str | None = None
    last_summary_by: str | None = None
    last_summary_at: str | None = None
    # CP1: live agent reasoning stream + a pending owner steer nudge.
    live_thoughts: list = field(default_factory=list)
    steer_message: str | None = None
    # CP2: master-plan spec (kind='plan' tickets awaiting approval in proposed).
    plan_spec: dict = field(default_factory=dict)


@dataclass
class Lesson:
    project_slug: str
    body: str
    task_slug: str = ""
    tags: list[str] = field(default_factory=list)
    created_at: str = ""


@dataclass
class AuditEntry:
    task_slug: str
    actor: str
    action: str
    detail: str = ""
    metadata: dict = field(default_factory=dict)
    created_at: str = ""


class CrewBoardStore:
    def __init__(self, db_path: Path) -> None:
        self._db_path = Path(db_path)
        self._db_path.parent.mkdir(parents=True, exist_ok=True)
        # check_same_thread=False so the gateway's worker threads can
        # share. We serialize ourselves via a single connection +
        # short transactions; SQLite's own locking handles the rest.
        self._conn = sqlite3.connect(
            self._db_path, timeout=5.0, check_same_thread=False,
        )
        self._conn.row_factory = sqlite3.Row
        # WAL mode allows a second writer process (vault_writer) to
        # coexist without SQLITE_BUSY errors. busy_timeout backs off
        # gracefully if a lock is held briefly.
        self._conn.execute("PRAGMA journal_mode=WAL")
        self._conn.execute("PRAGMA busy_timeout=5000")
        # Serialise all execute/commit calls so the store is genuinely
        # thread-safe even if a future code path calls it from a worker
        # thread (currently only the event-loop thread touches it, but
        # the lock is cheap insurance). RLock so internal helpers
        # (_audit, _next_task_slug) can be called from within an
        # already-locked public method without deadlocking.
        self._lock = threading.RLock()
        schema.apply(self._conn)

    def close(self) -> None:
        self._conn.close()

    # ---------------------------------------------------------------- helpers

    def _row_to_project(self, row: sqlite3.Row) -> Project:
        return Project(
            slug=row["slug"],
            path=row["path"],
            name=row["name"],
            enabled=bool(row["enabled"]),
            push_allowed=bool(row["push_allowed"]),
            test_cmd=row["test_cmd"],
            created_at=row["created_at"],
            updated_at=row["updated_at"],
            modified_at=(
                row["modified_at"] if "modified_at" in row.keys() else None
            ),
            parallel=bool(
                row["parallel"] if "parallel" in row.keys() else 0
            ),
        )

    def _row_to_task(self, row: sqlite3.Row) -> Task:
        return Task(
            slug=row["slug"],
            title=row["title"],
            body=row["body"],
            status=row["status"],
            project_slug=row["project_slug"],
            assignee=row["assignee"],
            created_by=row["created_by"],
            priority=row["priority"],
            estimate=row["estimate"],
            acceptance_criteria=json.loads(row["acceptance_criteria"]),
            files_of_interest=json.loads(row["files_of_interest"]),
            depends_on=json.loads(row["depends_on"]),
            tags=json.loads(row["tags"]),
            attempt_count=row["attempt_count"],
            last_branch=row["last_branch"],
            last_pr_url=row["last_pr_url"],
            verify_results=json.loads(row["verify_results"]),
            created_at=row["created_at"],
            updated_at=row["updated_at"],
            review_by=(
                row["review_by"] if "review_by" in row.keys() else None
            ),
            polish_iters=(
                row["polish_iters"] if "polish_iters" in row.keys() else None
            ),
            smoke_cmd=(
                row["smoke_cmd"] if "smoke_cmd" in row.keys() else None
            ),
            hive_tokens=(
                row["hive_tokens"] if "hive_tokens" in row.keys() else 0
            ) or 0,
            claude_tokens=(
                row["claude_tokens"] if "claude_tokens" in row.keys() else 0
            ) or 0,
            heartbeat_at=(
                row["heartbeat_at"] if "heartbeat_at" in row.keys() else None
            ),
            agent_turns=(
                row["agent_turns"] if "agent_turns" in row.keys() else 0
            ) or 0,
            parse_fails=(
                row["parse_fails"] if "parse_fails" in row.keys() else 0
            ) or 0,
            last_action=(
                row["last_action"] if "last_action" in row.keys() else None
            ),
            kind=(
                row["kind"] if "kind" in row.keys() and row["kind"] else "code"
            ),
            content_spec=(
                json.loads(row["content_spec"])
                if "content_spec" in row.keys() and row["content_spec"]
                else {}
            ),
            goal_id=(
                row["goal_id"] if "goal_id" in row.keys() else None
            ),
            board_id=(
                row["board_id"] if "board_id" in row.keys() and row["board_id"]
                else "default"
            ),
            last_summary=(
                row["last_summary"] if "last_summary" in row.keys() else None
            ),
            last_summary_by=(
                row["last_summary_by"] if "last_summary_by" in row.keys() else None
            ),
            last_summary_at=(
                row["last_summary_at"] if "last_summary_at" in row.keys() else None
            ),
            live_thoughts=(
                json.loads(row["live_thoughts"])
                if "live_thoughts" in row.keys() and row["live_thoughts"]
                else []
            ),
            steer_message=(
                row["steer_message"] if "steer_message" in row.keys() else None
            ),
            plan_spec=(
                json.loads(row["plan_spec"])
                if "plan_spec" in row.keys() and row["plan_spec"]
                else {}
            ),
        )

    def _row_to_audit(self, row: sqlite3.Row) -> AuditEntry:
        return AuditEntry(
            task_slug=row["task_slug"],
            actor=row["actor"],
            action=row["action"],
            detail=row["detail"],
            metadata=json.loads(row["metadata"]),
            created_at=row["created_at"],
        )

    def _next_task_slug(self) -> str:
        # Called from within an already-locked public method (RLock is reentrant).
        row = self._conn.execute(
            "SELECT next_n FROM crew_task_counter WHERE scope = 'task'"
        ).fetchone()
        n = int(row["next_n"]) if row else 1
        self._conn.execute(
            "UPDATE crew_task_counter SET next_n = ? WHERE scope = 'task'",
            (n + 1,),
        )
        return f"T-{n:04d}"

    def _audit(
        self, task_slug: str, actor: str, action: str,
        detail: str = "", metadata: dict | None = None,
    ) -> None:
        # Called from within an already-locked public method (RLock is reentrant).
        self._conn.execute(
            """
            INSERT INTO crew_audit
                (task_slug, actor, action, detail, metadata)
            VALUES (?, ?, ?, ?, ?)
            """,
            (
                task_slug, actor, action, detail,
                json.dumps(metadata or {}, separators=(",", ":")),
            ),
        )

    # ---------------------------------------------------------------- projects

    def upsert_project(self, p: Project) -> Project:
        with self._lock:
            existing = self._conn.execute(
                "SELECT slug FROM crew_projects WHERE slug = ?",
                (p.slug,),
            ).fetchone()
            if existing:
                self._conn.execute(
                    """
                    UPDATE crew_projects
                    SET path = ?, name = ?, test_cmd = ?, parallel = ?,
                        modified_at = COALESCE(?, modified_at),
                        updated_at = datetime('now')
                    WHERE slug = ?
                    """,
                    (
                        p.path, p.name, p.test_cmd, int(p.parallel),
                        p.modified_at, p.slug,
                    ),
                )
            else:
                self._conn.execute(
                    """
                    INSERT INTO crew_projects
                        (slug, path, name, enabled, push_allowed, test_cmd,
                         parallel, modified_at)
                    VALUES (?, ?, ?, ?, ?, ?, ?, ?)
                    """,
                    (
                        p.slug, p.path, p.name,
                        int(p.enabled), int(p.push_allowed), p.test_cmd,
                        int(p.parallel), p.modified_at,
                    ),
                )
            self._conn.commit()
            return self.get_project(p.slug)  # type: ignore[return-value]

    def get_project(self, slug: str) -> Project | None:
        with self._lock:
            row = self._conn.execute(
                "SELECT * FROM crew_projects WHERE slug = ?", (slug,),
            ).fetchone()
            return self._row_to_project(row) if row else None

    def list_projects(self, *, enabled_only: bool = False) -> list[Project]:
        with self._lock:
            if enabled_only:
                rows = self._conn.execute(
                    "SELECT * FROM crew_projects WHERE enabled = 1 ORDER BY slug"
                ).fetchall()
            else:
                rows = self._conn.execute(
                    "SELECT * FROM crew_projects ORDER BY slug"
                ).fetchall()
            return [self._row_to_project(r) for r in rows]

    def delete_project(self, slug: str) -> bool:
        """Delete a project row by slug. Returns True if a row was removed.

        Does NOT touch tasks — callers must ensure the project is empty (or
        intentionally orphan its tasks) first.
        """
        with self._lock:
            cur = self._conn.execute(
                "DELETE FROM crew_projects WHERE slug = ?", (slug,),
            )
            self._conn.commit()
            return cur.rowcount > 0

    def set_project_enabled(self, slug: str, *, enabled: bool) -> None:
        with self._lock:
            self._conn.execute(
                "UPDATE crew_projects SET enabled = ?, "
                "updated_at = datetime('now') WHERE slug = ?",
                (int(enabled), slug),
            )
            self._conn.commit()

    def set_project_push_allowed(self, slug: str, *, allowed: bool) -> None:
        with self._lock:
            self._conn.execute(
                "UPDATE crew_projects SET push_allowed = ?, "
                "updated_at = datetime('now') WHERE slug = ?",
                (int(allowed), slug),
            )
            self._conn.commit()

    def set_project_parallel(self, slug: str, *, parallel: bool) -> None:
        """Toggle worktree-per-task parallel lanes for a project."""
        with self._lock:
            self._conn.execute(
                "UPDATE crew_projects SET parallel = ?, "
                "updated_at = datetime('now') WHERE slug = ?",
                (int(parallel), slug),
            )
            self._conn.commit()

    # ---------------------------------------------------------------- tasks

    def create_task(
        self, *,
        title: str,
        project_slug: str,
        body: str = "",
        created_by: str = "owner",
        priority: str = "medium",
        estimate: str | None = None,
        acceptance_criteria: list[dict] | None = None,
        files_of_interest: list[str] | None = None,
        depends_on: list[str] | None = None,
        tags: list[str] | None = None,
        review_by: str | None = None,
        polish_iters: int | None = None,
        smoke_cmd: str | None = None,
        kind: str = "code",
        content_spec: dict | None = None,
        goal_id: str | None = None,
        board_id: str = "default",
    ) -> Task:
        if priority not in schema.PRIORITIES:
            raise ValueError(f"unknown priority {priority!r}")
        if estimate is not None and estimate not in schema.ESTIMATES:
            raise ValueError(f"unknown estimate {estimate!r}")
        # Bots land in proposed; owner-created go straight to backlog.
        status = (
            schema.STATUS_BACKLOG if created_by == "owner"
            else schema.STATUS_PROPOSED
        )
        with self._lock:
            slug = self._next_task_slug()
            self._conn.execute(
                """
                INSERT INTO crew_tasks
                    (slug, title, body, status, project_slug, created_by,
                     priority, estimate, acceptance_criteria,
                     files_of_interest, depends_on, tags,
                     review_by, polish_iters, smoke_cmd, kind, content_spec,
                     goal_id, board_id)
                VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
                """,
                (
                    slug, title, body, status, project_slug, created_by,
                    priority, estimate,
                    json.dumps(acceptance_criteria or []),
                    json.dumps(files_of_interest or []),
                    json.dumps(depends_on or []),
                    json.dumps(tags or []),
                    review_by, polish_iters, smoke_cmd,
                    kind, json.dumps(content_spec or {}),
                    goal_id, board_id,
                ),
            )
            self._audit(
                slug, created_by, "create",
                detail=f"created in {status}",
                metadata={"title": title, "project": project_slug},
            )
            self._conn.commit()
            return self.get_task(slug)  # type: ignore[return-value]

    def set_content_spec(self, slug: str, spec: dict) -> None:
        """Replace a content task's spec (request + result media ids)."""
        with self._lock:
            self._conn.execute(
                "UPDATE crew_tasks SET content_spec = ?, "
                "updated_at = datetime('now') WHERE slug = ?",
                (json.dumps(spec or {}), slug),
            )
            self._conn.commit()

