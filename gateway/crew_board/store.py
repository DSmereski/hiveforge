    def set_goal_id(self, slug: str, goal_id: str) -> None:
        """Stamp a task with the goal_id it belongs to (P6)."""
        with self._lock:
            self._conn.execute(
                "UPDATE crew_tasks SET goal_id = ? WHERE slug = ?",
                (goal_id, slug),
            )
            self._conn.commit()

    def list_tasks_by_goal_id(self, goal_id: str) -> list["Task"]:
        """All tasks stamped with goal_id (subtasks + verify tickets)."""
        with self._lock:
            rows = self._conn.execute(
                "SELECT * FROM crew_tasks WHERE goal_id = ? ORDER BY id",
                (goal_id,),
            ).fetchall()
            return [self._row_to_task(r) for r in rows]

    def get_task(self, slug: str) -> Task | None:
        with self._lock:
            row = self._conn.execute(
                "SELECT * FROM crew_tasks WHERE slug = ?", (slug,),
            ).fetchone()
            return self._row_to_task(row) if row else None

    def list_tasks(
        self, *,
        status: str | Iterable[str] | None = None,
        project_slug: str | None = None,
        assignee: str | None = None,
        board_id: str | None = None,
    ) -> list[Task]:
        clauses: list[str] = []
        params: list[Any] = []
        if status is not None:
            if isinstance(status, str):
                clauses.append("status = ?")
                params.append(status)
            else:
                statuses = list(status)
                if statuses:
                    clauses.append(
                        "status IN ("
                        + ",".join("?" * len(statuses)) + ")"
                    )
                    params.extend(statuses)
        if project_slug is not None:
            clauses.append("project_slug = ?")
            params.append(project_slug)
        if assignee is not None:
            clauses.append("assignee = ?")
            params.append(assignee)
        # P2 v-Next: when board_id is given, scope to that board only.
        # When None (back-compat default), return tasks from ALL boards.
        if board_id is not None:
            clauses.append("board_id = ?")
            params.append(board_id)
        sql = "SELECT * FROM crew_tasks"
        if clauses:
            sql += " WHERE " + " AND ".join(clauses)
        sql += " ORDER BY id"
        with self._lock:
            rows = self._conn.execute(sql, params).fetchall()
            return [self._row_to_task(r) for r in rows]

    def done_slugs(self) -> set[str]:
        """Set of done/archived task slugs. Lightweight (slug column only,
        no row hydration / JSON parse) — used by the dispatcher's per-tick
        depends_on check instead of materialising every done Task.
        Archived tasks are included so an archived upstream does not
        permanently block its dependents."""
        with self._lock:
            rows = self._conn.execute(
                "SELECT slug FROM crew_tasks WHERE status IN (?, ?)",
                (schema.STATUS_DONE, schema.STATUS_ARCHIVED),
            ).fetchall()
            return {r["slug"] for r in rows}

    def archive_old_done(self, retention_days: float) -> int:
        """Auto-archive done tasks whose last update is older than
        retention_days. Returns the number archived. retention_days <= 0
        disables the sweep (returns 0). Keeps the Done column bounded.

        Single bulk UPDATE — done -> archived is the only legal transition out
        of done, so no per-row transition check is needed. Deliberately skips
        the per-task audit row a manual move writes: this is automated
        maintenance, not an operator action."""
        if retention_days <= 0:
            return 0
        cutoff_secs = int(retention_days * 86400)
        with self._lock:
            cur = self._conn.execute(
                "UPDATE crew_tasks SET status = ?, updated_at = datetime('now') "
                "WHERE status = ? AND updated_at < datetime('now', ?)",
                (schema.STATUS_ARCHIVED, schema.STATUS_DONE,
                 f"-{cutoff_secs} seconds"),
            )
            n = cur.rowcount
            self._conn.commit()
        return int(n or 0)

    def move_task(
        self, slug: str, to_status: str,
        *, actor: str = "owner", detail: str = "",
    ) -> Task:
        with self._lock:
            task = self.get_task(slug)
            if task is None:
                raise ValueError(f"unknown task {slug!r}")
            if to_status not in schema.ALL_STATUSES:
                raise ValueError(f"unknown status {to_status!r}")
            if to_status not in schema.ALLOWED_TRANSITIONS.get(task.status, frozenset()):
                raise ValueError(
                    f"transition {task.status!r} -> {to_status!r} not allowed"
                )
            self._conn.execute(
                "UPDATE crew_tasks SET status = ?, "
                "updated_at = datetime('now') WHERE slug = ?",
                (to_status, slug),
            )
            self._audit(
                slug, actor, "move",
                detail=detail or f"{task.status} -> {to_status}",
                metadata={"from": task.status, "to": to_status},
            )
            self._conn.commit()
            return self.get_task(slug)  # type: ignore[return-value]

    def assign_task(
        self, slug: str, assignee: str, *, actor: str = "owner",
    ) -> Task:
        if assignee not in schema.ASSIGNEES:
            raise ValueError(f"unknown assignee {assignee!r}")
        with self._lock:
            task = self.get_task(slug)
            if task is None:
                raise ValueError(f"unknown task {slug!r}")
            self._conn.execute(
                "UPDATE crew_tasks SET assignee = ?, "
                "updated_at = datetime('now') WHERE slug = ?",
                (assignee, slug),
            )
            self._audit(
                slug, actor, "assign",
                detail=f"{task.assignee} -> {assignee}",
                metadata={"from": task.assignee, "to": assignee},
            )
            self._conn.commit()
            return self.get_task(slug)  # type: ignore[return-value]

    def set_depends_on(
        self, slug: str, depends_on: list[str], *, actor: str = "owner",
    ) -> Task:
        """Manually set a task's blockers (#172). The dispatcher won't claim the
        task until every depends_on slug is done. Drops self-reference + blanks;
        rejects unknown slugs and direct cycles."""
        with self._lock:
            task = self.get_task(slug)
            if task is None:
                raise ValueError(f"unknown task {slug!r}")
            deps: list[str] = []
            for raw in depends_on:
                d = str(raw).strip()
                if not d or d == slug or d in deps:
                    continue
                dep_task = self.get_task(d)
                if dep_task is None:
                    raise ValueError(f"unknown blocker {d!r}")
                if slug in (dep_task.depends_on or []):
                    raise ValueError(f"cycle: {d} already depends on {slug}")
                deps.append(d)
            self._conn.execute(
                "UPDATE crew_tasks SET depends_on = ?, "
                "updated_at = datetime('now') WHERE slug = ?",
                (json.dumps(deps), slug),
            )
            self._audit(
                slug, actor, "set_depends_on",
                detail=f"blocked-by {deps}", metadata={"depends_on": deps},
            )
            self._conn.commit()
            return self.get_task(slug)  # type: ignore[return-value]

    def set_review_by(self, slug: str, reviewer: str | None) -> Task:
        """Set (or clear) the reviewer that the dispatcher's review loop
        keys on. A task promoted to `review` with review_by=None is skipped
        by the loop forever (that skip is intentional ONLY for owner-parked
        failures) — so the qa-pass promotion must set this, else verified
        work strands in review (observed: T-0307)."""
        with self._lock:
            task = self.get_task(slug)
            if task is None:
                raise ValueError(f"unknown task {slug!r}")
            self._conn.execute(
                "UPDATE crew_tasks SET review_by = ?, "
                "updated_at = datetime('now') WHERE slug = ?",
                (reviewer, slug),
            )
            self._conn.commit()
