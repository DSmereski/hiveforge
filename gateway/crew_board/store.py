    def delete_task(self, slug: str) -> bool:
        """Hard-delete a task and all its child rows from the database.

        Removes the task from ``crew_tasks`` plus every related row keyed
        by slug: ``crew_audit`` entries, ``crew_approvals`` entries, and
        any lesson notes that reference this task in ``crew_lessons``.
        All deletes happen inside a single transaction so the DB is never
        left in a partial state.

        Returns True when a task row was found and deleted, False when the
        slug did not exist (so callers can return 404 cleanly).
        """
        with self._lock:
            row = self._conn.execute(
                "SELECT slug FROM crew_tasks WHERE slug = ?", (slug,),
            ).fetchone()
            if row is None:
                return False
            # Delete child rows first (no FK constraints, but cleaner).
            self._conn.execute(
                "DELETE FROM crew_audit WHERE task_slug = ?", (slug,),
            )
            self._conn.execute(
                "DELETE FROM crew_approvals WHERE task_slug = ?", (slug,),
            )
            # crew_lessons are keyed by project, but task_slug is stored
            # as a convenience field — remove the lesson notes that belong
            # to this task so no orphan detail remains.
            self._conn.execute(
                "DELETE FROM crew_lessons WHERE task_slug = ?", (slug,),
            )
            self._conn.execute(
                "DELETE FROM crew_tasks WHERE slug = ?", (slug,),
            )
            self._conn.commit()
            return True

