    def add_tokens(self, slug: str, *, kind: str, n: int) -> None:
        """Accumulate token usage on a task. kind must be 'hive' or
        'claude' — the two are tracked in separate columns and NEVER
        combined."""
        if kind not in ("hive", "claude"):
            raise ValueError(f"token kind must be hive|claude, got {kind!r}")
        if n <= 0:
            return
        col = "hive_tokens" if kind == "hive" else "claude_tokens"
        with self._lock:
            self._conn.execute(
                f"UPDATE crew_tasks SET {col} = COALESCE({col}, 0) + ? "
                "WHERE slug = ?",
                (int(n), slug),
            )
            self._conn.commit()

    def tokens_by_day(self, days: int = 30) -> list[dict]:
        """Aggregate token usage per calendar day for the last *days* days.

        Groups ``crew_tasks`` rows by ``date(updated_at)`` — the date the
        task last accumulated tokens (best available per-day proxy; tokens
        accrue via ``record_turn`` / ``add_tokens``, which both touch
        ``updated_at`` via the task path).  Returns a list of dicts::

            [{"date": "YYYY-MM-DD", "hive": int, "claude": int, "total": int}, ...]

        Ascending by date, always exactly *days* entries, zero-filled for
        days with no recorded activity so the line chart never has gaps.
        Only the last *days* calendar days (today inclusive, UTC) are
        considered — older data is dropped to keep the response tight.
        """
        days = max(1, int(days))
        with self._lock:
            rows = self._conn.execute(
                """
                SELECT
                    date(updated_at) AS day,
                    COALESCE(SUM(hive_tokens),   0) AS hive,
                    COALESCE(SUM(claude_tokens), 0) AS claude
                FROM crew_tasks
                WHERE updated_at >= date('now', ? || ' days')
                GROUP BY day
                ORDER BY day ASC
                """,
                (f"-{days - 1}",),
            ).fetchall()

        # Build a lookup so we can zero-fill missing days.
        by_day: dict[str, dict] = {
            r["day"]: {"hive": int(r["hive"]), "claude": int(r["claude"])}
            for r in rows
        }

        import datetime as _dt
        today = _dt.date.today()
        result: list[dict] = []
        for i in range(days - 1, -1, -1):
            d = today - _dt.timedelta(days=i)
            key = d.isoformat()
            entry = by_day.get(key, {"hive": 0, "claude": 0})
            result.append({
                "date":   key,
                "hive":   entry["hive"],
                "claude": entry["claude"],
                "total":  entry["hive"] + entry["claude"],
            })
        return result

    def rolling_24h_claude_cost_usd(
        self, *, usd_per_million_tokens: float = 6.0,
    ) -> float:
        """Return estimated USD spent on claude escalations in the last 24 hours.

        Sums claude_tokens across all tasks whose updated_at falls within the
        last 24 hours and converts using the same blended rate used by the
        /board/stats endpoint ($6 / 1M tokens by default).

        The 24-hour window uses SQLite's datetime('now', '-24 hours') so it is
        always relative to the current UTC wall clock — no timezone conversion
        needed.
        """
        with self._lock:
            row = self._conn.execute(
                """
                SELECT COALESCE(SUM(claude_tokens), 0) AS total
                FROM crew_tasks
                WHERE updated_at >= datetime('now', '-24 hours')
                """,
            ).fetchone()
            total_tokens = int(row["total"]) if row else 0
        return total_tokens / 1_000_000 * usd_per_million_tokens

