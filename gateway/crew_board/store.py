    def relevant_lessons(
        self,
        project_slug: str,
        task_title: str,
        task_body: str,
        *,
        limit: int = 3,
    ) -> list[Lesson]:
        """Return up to *limit* lessons for a project ranked by keyword
        overlap with *task_title* + *task_body*, with recency as a
        tiebreaker.  Mirrors the ``_relevant_skills`` scorer in
        ``hive_agent_loop.py``: extract 4+-character words from the task
        text and count how many appear in each lesson body.  Lessons with
        no overlap are still eligible so a project with few lessons
        doesn't produce an empty result."""
        import re as _re

        # Fetch all lessons for the project (newest first so recency wins
        # ties with equal score).
        with self._lock:
            rows = self._conn.execute(
                "SELECT * FROM crew_lessons WHERE project_slug = ? "
                "ORDER BY id DESC",
                (project_slug,),
            ).fetchall()
        if not rows:
            return []

        lessons = [
            Lesson(
                project_slug=r["project_slug"], body=r["body"],
                task_slug=r["task_slug"],
                tags=json.loads(r["tags"] or "[]"),
                created_at=r["created_at"],
            )
            for r in rows
        ]

        # Build the keyword set from the task text (same regex as
        # _relevant_skills: 4+ lowercase alpha characters).
        query_words = set(
            _re.findall(r"[a-z]{4,}", (task_title + " " + task_body).lower())
        )

        def _score(lesson: Lesson) -> int:
            hay = lesson.body.lower()
            return sum(1 for w in query_words if w in hay)

        # Sort descending by score; within the same score the earlier
        # position (higher id = newer = lower index in `lessons`) is
        # preserved because Python's sort is stable.
        ranked = sorted(lessons, key=_score, reverse=True)
        return ranked[:limit]

